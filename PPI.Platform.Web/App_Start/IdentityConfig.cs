    public class EmailService : IIdentityMessageService
    {
        public Task SendAsync(IdentityMessage message)
        {
            // Plug in your email service here to send an email.
            return configSendGridasync(message);
        }

        private Task configSendGridasync(IdentityMessage message)
        {
            
            var Message = new MailMessage();
            Message.To.Add(new MailAddress(message.Destination));
            Message.From = new MailAddress(ConfigurationManager.AppSettings["OWINAccountFromAddress"]);
            Message.Subject = message.Subject;
            Message.Body = message.Body;
            Message.IsBodyHtml = true;

            // Commented out until Email Util is finished in the System IT section
            
            
            var Credentials = new NetworkCredential(
                PPI.Platform.Core.Utility.MachineKeyWrapper.UnprotectUrlSafeString(ConfigurationManager.AppSettings["mailAccount"], "configuration"),
                PPI.Platform.Core.Utility.MachineKeyWrapper.UnprotectUrlSafeString(ConfigurationManager.AppSettings["mailPassword"], "configuration")
                 );
            // Create a Web transport for sending email.
            //This is where MEF as an IOC fails down because this isn't a controller
            IEmail _IEmail = new SendGridEmail();
            return _IEmail.SendAsync(Message, new EmailConfiguration() { Credentials = Credentials });
                        
        }
    }

